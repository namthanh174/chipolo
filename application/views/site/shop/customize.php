                    checkValidation = function ()
                    {
                        var valid = true;
                        var inputs = $(".hidden-selection");
                        $.each(inputs, function (index, input) {
                            var el = $(input);
                            if (el.val() == "") {
                                valid = false;
                            }
                        });
                        if (!valid) {
                            $("input[type=submit]").addClass("disabled");
                        } else {
                            $("input[type=submit]").removeClass("disabled");
                        }
                        return valid;
                    };

