                            <div class="faq-1" style="display: none;">
                                <hr/>
                                <p><p>The Chipolo is a Bluetooth 4.0 enabled device that you can drop or attach to items that you may misplace and using the Chipolo iOS, Android or Windows Phone application to find.</p></p>

