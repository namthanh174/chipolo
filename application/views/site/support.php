                            <p class="lead"><a id="faq-2" href="#category-1/faq-2" class="qna" data-cid="2">How does the Chipolo work?</a></p>
                            <div class="faq-2" style="display: none;">
                                <hr/>
                                <p><p>The Chipolo is a Bluetooth 4.0 enabled device that you can attach to items that you may misplace and using the Chipolo iOS, Android or Windows Phone application to find.</p>
                                <p>The Chipolo locations are updated using the Bluetooth radio connection between the Chipolo and the Chipolo App running on your iOS, Android. The Chipolo App "discovers" the Chipolo using the Bluetooth signal and then uses the services of the iOS, Android or Windows Phone device to update the location information for that Chipolo. The Chipolo needs to be within the Bluetooth range of approximately 200 feet/60 meters of the iOS, Android or Windows Phone device to be discovered.</p>
                                <p>This is where the "Community Search&rdquo; feature comes in to play. If any Chipolo Network member running the Chipolo App comes in range of a Chipolo, their App will send an anonymous update of that Chipolo&rsquo;s location to our system, helping the owner to locate that item even if they are not nearby.</p>
                                <p>As the Chipolo Network community of users running the app grows, the more effective our network will be in finding your lost item.</p></p>

